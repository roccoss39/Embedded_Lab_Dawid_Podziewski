void ToggleLEDAndReportState(uint16_t GPIO_Pin, uint8_t diodeNumber) {
	GPIO_PinState pinState = HAL_GPIO_ReadPin(GPIOD, GPIO_Pin);

	static char message[25];

	if (pinState == GPIO_PIN_SET) {
		snprintf(message, sizeof(message), "Diode %d - OFF\r\n", diodeNumber);
	} else {
		snprintf(message, sizeof(message), "Diode %d - ON\r\n", diodeNumber);
	}
	message[sizeof(message) - 1] = '\0';

	HAL_UART_Transmit_DMA(&huart3, (uint8_t*) message, strlen(message));
	HAL_GPIO_TogglePin(GPIOD, GPIO_Pin);
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
	if (GPIO_Pin == SWITCH_1) {
		ToggleLEDAndReportState(DIODE_1, 1);
	} else if (GPIO_Pin == SWITCH_2) {
		ToggleLEDAndReportState(DIODE_2, 2);
	} else if (GPIO_Pin == SWITCH_3) {
		ToggleLEDAndReportState(DIODE_3, 3);
	} else if (GPIO_Pin == SWITCH_4) {
		ToggleLEDAndReportState(DIODE_4, 4);
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
	if (htim->Instance == TIM1) {
		HAL_ADC_Start_IT(&hadc1);
	}
	if (htim->Instance == TIM4) {
		HAL_ADC_Start_IT(&hadc2);
	}
}

void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc) {

	const float SupplyVoltage = 3.04; // [checked by multimeter]
	const float ResolutionADC = 4095.0;

	if (hadc->Instance == ADC1) {

		const float Avg_slope = 0.0025;
		const float V25 = 0.76;

		uint16_t TemperatureADC = HAL_ADC_GetValue(&hadc1);
		float TVoltage = (SupplyVoltage * TemperatureADC) / ResolutionADC;
		float Temperature = ((TVoltage - V25) / Avg_slope) + 25;

		char message[30];
		snprintf(message, sizeof(message), "Temperature: %.2f\r\n",
				Temperature);
		HAL_UART_Transmit_DMA(&huart3, (uint8_t*) message, strlen(message));
	}

	if (hadc->Instance == ADC2) {

		uint16_t ResistanceADC = HAL_ADC_GetValue(&hadc2);
		float Voltage = (SupplyVoltage * ResistanceADC) / ResolutionADC;

		char message[30];
		snprintf(message, sizeof(message), "Voltage: %.2f\r\n", Voltage);
		HAL_UART_Transmit_DMA(&huart3, (uint8_t*) message, strlen(message));
	}
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {

	switch (Received) {

	case '1':
		ToggleLEDAndReportState(DIODE_1, 1);
		break;
	case '2':
		ToggleLEDAndReportState(DIODE_2, 2);
		break;
	case '3':
		ToggleLEDAndReportState(DIODE_3, 3);
		break;
	case '4':
		ToggleLEDAndReportState(DIODE_4, 4);
		break;
	default:
		HAL_UART_Transmit_DMA(&huart3, (uint8_t*) " - UnexpCmd", 14);
		break;
	}
	HAL_UART_Receive_IT(huart, &Received, 1);
}
